public class Main {
    public static void main(String[] args) {
        int a = 120;

        // Пример 1: byte b = a + 10; 
        // Операция a + 10 выполняется в типе int, что приводит к переполнению, так как результат не помещается в byte.
        byte b = (byte) (a + 10); // Преобразуем результат в byte, выполняется явное приведение типа.
        System.out.println("b = " + b); // Вывод: b = -126

        // Пример 2: byte c = (byte)(a + 10);
        // Операция a + 10 выполняется в int, но результат сразу приводится к byte, что также приводит к переполнению.
        byte c = (byte) (a + 10); // Преобразуем результат в byte с явным кастом.
        System.out.println("c = " + c); // Вывод: c = -126

        // Пример 3: byte d = a + 1;
        // Операция a + 1 выполняется в int, а затем результат преобразуется в byte.
        // Для byte нет явного приведения типа, так как компилятор не позволяет преобразовать int в byte без явного кастинга.
        byte d = (byte) (a + 1); // Преобразуем результат в byte.
        System.out.println("d = " + d); // Вывод: d = 121
    }
}
