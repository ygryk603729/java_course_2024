public class Main {
    public static void main(String[] args) {
        int a = 2; // Переменная для проверки switch
        int i = 0; // Значение i изменится в зависимости от case

        switch (a) {
            case 1: // Если a == 1
                i = 1;
                break; // Завершаем выполнение блока
            case 2: // Если a == 2
                i = 2; // i получает значение 2
                // break отсутствует, выполнение продолжается в case 3
            case 3: // Выполнение попадает сюда из case 2
                i = 3; // i перезаписывается значением 3
                break; // Завершаем выполнение
            default: // Выполняется, если ни один из case не совпал
                i = -1;
        }

        // Вывод результата для проверки
        System.out.println("Значение i: " + i);
    }
}

/*
Объяснение:
1. Если a == 1:
   - Войдём в case 1, i станет 1, и выполнение завершится из-за break.
   - Результат: i = 1.
2. Если a == 2:
   - Войдём в case 2, i станет 2.
   - Из-за отсутствия break выполнение продолжится в case 3, где i перезапишется значением 3.
   - Результат: i = 3.
3. Если a == 3:
   - Войдём в case 3, i станет 3, и выполнение завершится из-за break.
   - Результат: i = 3.
4. Если a не равен 1, 2 или 3:
   - Войдём в default, i станет -1.
   - Результат: i = -1.

Это поведение называется *проваливанием* (fall-through), и оно может быть полезным, если нужно обработать несколько случаев схожим образом. Однако отсутствие break может привести к неожиданным ошибкам.
*/
