public class Main {
    public static void main(String[] args) {
        // Бесконечный цикл с использованием while
        while (true) {
            System.out.println("Бесконечный цикл while");
            // Чтобы цикл был бесконечным, никакого выхода не предусмотрено
        }

        // Пример бесконечного цикла с do-while
        do {
            System.out.println("Бесконечный цикл do-while");
            // Цикл будет выполняться бесконечно, так как условие всегда true
        } while (true);
    }
}